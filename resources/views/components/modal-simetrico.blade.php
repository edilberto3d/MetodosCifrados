                    <tr>
                        <td>Clave única</td>
                        <td>Se utiliza la misma clave para cifrar y descifrar el mensaje.</td>
                    </tr>
                    <tr>
                        <td>Rapidez</td>
                        <td>Es más rápido que los cifrados asimétricos debido a su simplicidad.</td>
                    </tr>
                    <tr>
                        <td>Distribución de la clave</td>
                        <td>Ambas partes deben compartir la clave de manera segura para evitar que sea interceptada.</td>
                    </tr>
                    <tr>
                        <td>Usos Modernos</td>
                        <td>Se utiliza en VPNs, cifrado de archivos y almacenamiento seguro de datos.</td>
                    </tr>
