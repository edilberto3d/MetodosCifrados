    <div class="modal-body text-start">
        <p class="modal-text">
            El Cifrado Simétrico es un método de cifrado en el que la misma clave se utiliza tanto para cifrar como para descifrar la información. Este tipo de cifrado es rápido y se utiliza comúnmente en sistemas donde la velocidad es crucial, como el cifrado de datos en discos duros o en redes privadas virtuales (VPN).
        </p>

        <h5 class="text-center">Historia del Cifrado Simétrico:</h5>
        <p class="modal-text">
            Los métodos de cifrado simétrico se han utilizado durante siglos. En la era moderna, algoritmos como el DES (Data Encryption Standard) y su sucesor AES (Advanced Encryption Standard) han sido ampliamente adoptados en el mundo de la informática y la seguridad de la información.
        </p>

        <h5 class="text-center">Pasos para usar el Cifrado Simétrico:</h5>
        <ol class="steps-list">
            <li>Escribe el mensaje que deseas cifrar o descifrar en el campo de texto.</li>
            <li>Introduce la clave secreta que ambos, emisor y receptor, deben conocer.</li>
            <li>Haz clic en el botón "Cifrar" para encriptar el mensaje o "Descifrar" para obtener el texto original.</li>
            <li>Puedes copiar el resultado con el botón "Copiar Texto" para pegarlo en otro lugar.</li>
        </ol>

        <h5 class="text-center">Ejemplo de Cifrado Simétrico:</h5>
        <p class="modal-text">Con una clave secreta, como "clave123", un mensaje como "Hola Mundo" se transforma en un texto cifrado ilegible.</p>

        <h5 class="text-center">Características del Cifrado Simétrico:</h5>
        <div class="table-responsive">
            <table class="table table-bordered">
