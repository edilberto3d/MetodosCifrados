        <h5 class="text-center">Aplicaciones Modernas:</h5>
        <p class="modal-text">
            Aunque el cifrado simétrico es seguro y eficiente, su principal desafío es la distribución de la clave. Hoy en día, es común combinarlo con otros métodos de cifrado (como el asimétrico) para mejorar la seguridad.
        </p>

