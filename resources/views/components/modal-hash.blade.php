        <h4 class="modal-title text-center">¿Qué es el Hashing?</h4>
        <button type="button" class="btn-close" data-bs-dismiss="modal" aria-label="Close"></button>
    </div>
    <div class="modal-body">
        <p class="text-left">
            El hashing es un proceso criptográfico que transforma datos de cualquier tamaño en una cadena de longitud fija, conocida como "hash" o "digest". A diferencia de los cifrados, los hashes no pueden ser revertidos a sus datos originales, lo que los hace útiles para verificación de integridad y almacenamiento seguro de contraseñas.
        </p>

        <h5 class="text-center">¿Cómo se utiliza el Hashing en la Aplicación?</h5>
        <p class="text-left">
            Sigue estos pasos para aplicar los algoritmos de hash en la aplicación:
        </p>
        <ol class="text-left">
            <li>Selecciona el algoritmo de hash que deseas utilizar, como RIPEMD, RIPEMD-128, RIPEMD-160, entre otros.</li>
            <li>Ingresa el mensaje o texto en el campo correspondiente.</li>
            <li>Haz clic en "Generar Hash" para obtener el resumen criptográfico del texto.</li>
            <li>El resultado del hash se mostrará en pantalla. Puedes copiarlo usando el botón "Copiar Texto".</li>
        </ol>

        <h5 class="text-center">¿Qué es RIPEMD?</h5>
        <p class="text-left">
            RIPEMD (RACE Integrity Primitives Evaluation Message Digest) es una familia de funciones de hash criptográficas desarrollada en Europa en 1992. Existen varias versiones de RIPEMD, que difieren en la longitud de los resúmenes generados, como RIPEMD-128, RIPEMD-160, RIPEMD-256 y RIPEMD-320.
        </p>

        <h5 class="text-center">¿Para qué se usa el Hashing?</h5>
        <p class="text-left">
            El hashing es comúnmente utilizado para almacenar contraseñas de forma segura, ya que el resumen criptográfico no puede ser convertido de nuevo a la contraseña original. También es útil en la verificación de integridad, como en la detección de cambios no autorizados en archivos o mensajes.
        </p>

        <h5 class="text-center">Diferentes Algoritmos de Hash en la Aplicación</h5>
        <p class="text-left">
            En esta aplicación, puedes elegir entre diferentes algoritmos de hash:
        </p>
        <ul class="text-left">
            <li><strong>RIPEMD:</strong> Genera un resumen de 128 bits.</li>
            <li><strong>RIPEMD-128:</strong> Función de hash que genera un digest de 128 bits.</li>
            <li><strong>RIPEMD-160:</strong> Amplía el tamaño del resumen a 160 bits, proporcionando una mayor resistencia a colisiones.</li>
            <li><strong>RIPEMD-256:</strong> Función de hash que genera un resumen de 256 bits, ideal para aplicaciones que requieren más seguridad.</li>
            <li><strong>RIPEMD-320:</strong> Genera un hash de 320 bits, ofreciendo una mayor longitud para aplicaciones con altos requerimientos de seguridad.</li>
        </ul>

