        <p class="modal-text">
            El Cifrado Asimétrico es un método de cifrado que utiliza un par de claves: una clave pública para cifrar los datos y una clave privada para descifrarlos. Este tipo de cifrado es ampliamente utilizado en la seguridad informática moderna, como en el intercambio seguro de datos a través de Internet.
        </p>

        <h5 class="text-center">Historia del Cifrado Asimétrico:</h5>
        <p class="modal-text">
            El cifrado asimétrico fue propuesto en la década de 1970 como una solución al problema de compartir claves de manera segura. El algoritmo RSA, desarrollado en 1977, es uno de los más conocidos y se sigue utilizando hoy en día. Otros algoritmos populares incluyen ECC (Elliptic Curve Cryptography) y DSA (Digital Signature Algorithm).
        </p>

        <h5 class="text-center">Simulación de Cifrado Asimétrico:</h5>
        <p class="modal-text">A continuación, te mostraremos una simulación de los tres métodos de cifrado asimétrico:</p>

        <h6>1. Diffie-Hellman (Intercambio de claves):</h6>
        <p class="modal-text">
            Este algoritmo permite que dos partes generen una clave compartida utilizando sus claves privadas, sin que la clave compartida sea transmitida directamente. En esta simulación, verás cómo se genera una clave compartida utilizando una operación modular.
        </p>

        <h6>2. RSA (Cifrado y Descifrado):</h6>
        <p class="modal-text">
            RSA es uno de los métodos más conocidos. Utiliza una clave pública para cifrar los datos y una clave privada para descifrarlos. En nuestra simulación, se utiliza una operación XOR para demostrar el principio de RSA.
        </p>

        <h6>3. ECC (Elliptic Curve Cryptography):</h6>
        <p class="modal-text">
            La criptografía de curva elíptica (ECC) es un enfoque avanzado para el cifrado asimétrico. Funciona con claves más pequeñas y es más eficiente que RSA. En la simulación, se utiliza también una operación XOR para mostrar cómo funciona el proceso.
        </p>




